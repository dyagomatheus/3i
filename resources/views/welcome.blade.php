<b>TERMO DE GARANTIA E POLÍTICA DE AUTORIZAÇÃO PARA RETORNO DE MERCADORIA (RMA)</b><br><br>



<b>CARTUCHOS DE TONER COMPATÍVEIS:</b><br>
Todos os produtos possuem garantia contra defeitos de fabricação durante 12 (doze) meses, a contar da data de emissão da NF (Nota Fiscal) de Venda<br><br>



<b>CARTUCHOS DE TINTA COMPATÍVEIS:</b><br>
Todos os produtos possuem garantia contra defeitos de fabricação durante 6 (seis) meses a contar da data de emissão da NF (Nota Fiscal) de Venda.<br><br>



<b>CARTUCHOS DE JATO DE TINTA ORIGINAIS:</b><br>
Todos os produtos possuem garantia contra defeitos de fabricação durante 3 (três) meses a contar da data de emissão da NF (Nota Fiscal) de Venda.<br><br>



<b>INSUMOS PARA REMANUFATURA:</b><br>
Todos os produtos possuem garantia contra defeitos de fabricação durante 3 (três) meses a contar da data de emissão da NF (Nota Fiscal) de Venda.<br><br>


<b>PODEM SER CONSIDERADOS DEFEITOS DE FABRICAÇÃO:</b><br>
•   Erros de leitura, não reconhecer o produto instalado (desde que utilizados na impressora correta<br>
•   Marcas ou manchas na impressão<br>
•   Impressão fraca<br>
•   Toner ou tinta vazando<br>
•   Papel trancando no cartucho de toner<br>
•   Estrutura do cartucho de tinta ou toner irregular<br><br>



<b>MOTIVOS PARA PERDA DE GARANTIA:</b><br>
•    Produto que não pertença ou que não tenha sido vendido pela 3i Distribuidora<br>
•    Descaracterização, sem etiquetas de modelo, lote de fabricação e código de barras<br>
•    Lacres violados<br>
•    Pinos quebrados<br>
•    Produtos com sinais de remanufatura ou recarga<br>
•    Marcas de furo por seringa<br>
•    Parafusos marcados por chave<br>
•    Riscos no cilindro ocasionados por má utilização, conservação ou acomodação<br>
•    Danos causados por quedas ou má aplicação do produto, tal como forçar a entrada em equipamento diferente de sua especificação ou alterar sua configuração para múltiplo uso<br>
•    Produtos vazios (utilizar tabela de pesos fornecido pela 3i Distribuidora, conforme marcas e modelos).<br><br>



<b>REGRAS GERAIS:</b><br>
Os produtos devem estar acompanhados do formulário interno de RMA e Nota Fiscal de Devolução, conforme especificação abaixo:<br>
Não serão aceitos produtos sem o correto preenchimento do <b><u>formulário de RMA e da Nota Fiscal de Devolução</u></b>.<br>
O formulário de RMA pode ser acessado diretamente no site da 3i Distribuidora (www.3idistribuidora.com.br/RMA).<br>
As notas fiscais deverão seguir o modelo fornecido pela 3i Distribuidora. Não serão aceitos produtos sem a respectiva nota fiscal.<br>
Os produtos devem estar acompanhados de folha teste e passarão por análise, onde será averiguado, peso, condições gerais do produto e efetuado testes de qualidade de impressão.<br>
O prazo para análise é de 72 horas, a contar do recebimento dos produtos.<br>
Após as análises a garantia poderá ser autorizada ou negada. Os produtos autorizados serão enviados ao cliente/revendedor, respeitando os modelos e marcas aprovados. Os produtos negados, serão enviados ao cliente em conjunto com os produtos aprovados.<br>
Em casos onde todos os produtos sejam negados, os mesmos ficarão à disposição do cliente para ser retirado na 3i Distribuidora, pelo período de até 90 dias.<br>
Não poderá haver liberação parcial de produtos aprovados no processo de RMA.<br><br>



<b>INSTRUÇÕES PARA DEVOLUÇÕES:</b><br>
•   O cliente/revendedor deverá informar à 3i Distribuidora sobre os produtos que apresentaram defeito e sua intenção em trocá-los.<br>
•   A comunicação deve ser feita através do site www.3idistribuidora.com.br/formulario-rma.<br>
•   O cliente/revendedor deverá remeter o produto até a 3i Distribuidora, acompanhado do formulário de RMA devidamente preenchido e com Nota Fiscal de Devolução, conforme o CFOP abaixo.<br><br>



<b>FRETE:</b><br>
a.  a) O frete de remessa para conserto ocorre por conta do cliente, salvo em casos cujo erro for comprovadamente da 3i Distribuidora. Em outras situações, não receberemos materiais com frete a pagar<br>
b.  b) O frete de retorno é de responsabilidade da 3i Distribuidora desde que o produto esteja dentro do período e normas de garantia descritas acima, assim como nas INSTRUÇÕES PARA DEVOLUÇÃO DE MERCADORIA<br>
c.  c) Produtos sem defeito ficarão disponíveis para retirada em até 90 dias, neste caso o frete é de responsabilidade do solicitante do RMA<br><br>



<b>PRENCHIMENTO CORRETO DO CFOP:</b><br>
• Para devoluções realizadas dentro do Estado de origem, as notas fiscais deverão ser preenchidas da seguinte forma:<br>
• <b>Remessa para Troca, conserto e/ou simples remessa – CFOP 5.949 (Remessa para conserto ou simples remessa):</b> discriminar nas informações complementares (Dados Adicionais) as informações da Nota Fiscal de Venda, como número da Nota Fiscal e data da emissão. <b>Seguir modelo fornecido pela 3i Distribuidora</b>.<br><br>


• Para devoluções realizadas fora do estado de origem, as devoluções deverão ser preenchidas da seguinte forma:<br>
• <b>Remessa para Troca, conserto e/ou simples remessa – CFOP 6.949< (Remessa para conserto ou simples remessa):</b> discriminar nas informações complementares (Dados Adicionais) as informações da Nota Fiscal de Venda, como número da Nota Fiscal e data da emissão. <b>Seguir modelo fornecido pela 3i Distribuidora</b>.<br><br>



<b>CANCELAMENTO DA COMPRA DE PRODUTOS SEM USO E/OU DEFEITO:</b><br>
Segundo o Código de Defesa do Consumidor (CDC) – Lei 8.078/90 Artigo 49 e § único, na devolução de produtos que não apresentam defeito e/ou foram solicitados erroneamente pelo cliente, a compra poderá ser cancelada desde que a 3i Distribuidora concorde com seu cancelamento e a data da compra não tenha excedido o prazo máximo de 7 dias da emissão da Nota Fiscal de Venda.<br>
Somente serão aceitos mercadorias devidamente lacradas.<br>
O frete de retorno da mercadoria para a 3i Distribuidora ocorre por conta do cliente/revendedor.<br>
Os produtos deverão estar acompanhados de nota fiscal de devolução, conforme modelo fornecido.<br><br>



<b>PRENCHIMENTO CORRETO DO CFOP:</b><br>
• Para devoluções realizadas dentro do Estado de origem, as notas fiscais deverão ser preenchidas da seguinte forma:<br>
• <b>Devolução de Mercadoria – CFOP 5.202 para produtos sem ST ou 5.411 para produtos com ST:</b> Discriminar nas informações complementares (Dados Adicionais) as informações da Nota Fiscal de Venda, como número da Nota Fiscal e data da emissão. <b>Seguir modelo fornecido pela 3i Distribuidora.</b><br><br>


• Para devoluções realizadas fora do estado de origem, as devoluções deverão ser preenchidas da seguinte forma:<br>
• <b>Devolução de Mercadoria – CFOP 6.202 para produtos sem ST ou 6.411 para produtos com ST:</b> Discriminar nas informações complementares (Dados Adicionais) as informações da Nota Fiscal de Venda, como número da Nota Fiscal e data da emissão. <b>Seguir modelo fornecido pela 3i Distribuidora.</b><br><br><br>


